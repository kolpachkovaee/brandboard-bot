import google.generativeai as genai
import config

genai.configure(api_key=config.GEMINI_API_KEY)
model = genai.GenerativeModel("gemini-1.5-flash")


async def analyze_brief(data: dict) -> str:
    prompt = f"""Ты — опытный арт-директор и бренд-стратег. 
На основе брифа составь визуальную концепцию бренда.

БРИФ:
Бренд: {data.get('brand', '—')}
Продукт: {data.get('product', '—')}
Миссия и ценности: {data.get('mission', '—')}
Целевая аудитория: {data.get('audience', '—')}
Боль клиента: {data.get('pain', '—')}
Конкуренты и отличие: {data.get('competitors', '—')}
Визуальные референсы: {data.get('visual_like', '—')}
Желаемое настроение: {data.get('mood', '—')}
Ключевые слова: {data.get('words', '—')}
Бренд как человек: {data.get('person', '—')}

Напиши красивый структурированный мудборд для Telegram.
Используй Markdown: *жирный* для заголовков разделов.
Используй эмодзи для визуального разделения.
Пиши профессионально, образно, вдохновляюще.

Структура строго такая:

✦ *[НАЗВАНИЕ БРЕНДА] — [АРХЕТИП]*

*Концепция*
[2–3 предложения — суть визуальной идеи, поэтично и точно]

*🎨 Цветовая палитра*
[Основной]: [название цвета] — #[hex]
[Поддерживающий]: [название цвета] — #[hex]  
[Акцент]: [название цвета] — #[hex]
[1–2 предложения о настроении палитры]

*✍️ Типографика*
Заголовки: [название шрифта] — [почему подходит]
Текст: [название шрифта]
[1 предложение о стиле]

*📸 Образный ряд*
→ [сцена или образ 1]
→ [сцена или образ 2]
→ [сцена или образ 3]
→ [сцена или образ 4]

*🚫 Избегать*
→ [что не должно быть в визуале 1]
→ [что не должно быть в визуале 2]

*🔖 Референсы*
[5–6 брендов или художников через запятую]

*💬 Идеи слоганов*
«[вариант 1]»
«[вариант 2]»
«[вариант 3]»

*✉️ Дизайнеру*
[1–2 предложения — самое важное что нужно передать визуалом]"""

    response = model.generate_content(prompt)
    return response.text
